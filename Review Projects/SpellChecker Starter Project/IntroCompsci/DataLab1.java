package IntroCompsci;


class DataLab1{

    public static void main (String[] args){

        double x= 0.0;

        double result = (3*x*x) - (8*x) + 4;

        System.out.println("At x = " + x + ", 3x^2-8x+4 = " + result);

        x = 2.0;

        result = (3*x*x) - (8*x) + 4;

        System.out.println("\nAt x = " + x + ", 3x^2-8x+4 = " + result);
        
        x= 4.0;
        
        result = (3*x*x) - (8*x) + 4;
        
        System.out.println("\nAt x = " + x + ", 3x^2-8x+4 = " + result);
     

    }


}